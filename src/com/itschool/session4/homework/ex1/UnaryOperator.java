package com.itschool.session4.homework.ex1;

public class UnaryOperator {
    public static void main(String[] args) {
        //init var
        int nr = 10;

        System.out.println("Initial alue: " + nr);

        //increment var
        nr++;
        System.out.println("After increment: " + nr);

        nr--;
        System.out.println("After decrement: " + nr);

        nr += 5;
        System.out.println("After adding by 5: " + nr);

        nr -= 3;
        System.out.println("After subtracting by 3: " + nr);

    }
}
